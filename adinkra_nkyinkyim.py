from ColabTurtlePlus.Turtle import *

setup(2000,2000)

def repete_um():
  fd(120)
  left(90)
  fd(20)
  left(90)

def repete_dois():
  fd(120)
  left(-90)
  fd(20)
  left(-90)

def repete_tres():
  left(90)
  fd(40)
  left(-90)
  fd(20)
  bk(20)

def nkyinkyim():
  initializeTurtle()
  speed(13)
  pensize(15)
  left(180)
  for _ in range(2):
    repete_um()
    repete_dois()
  fd(120)
  left(90)
  fd(20)
  bk(20)
  for _ in range(3):
    repete_tres()
  saveSVG("nkyinkyim.svg", False)

nkyinkyim()
